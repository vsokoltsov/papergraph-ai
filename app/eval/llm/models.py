from __future__ import annotations

from dataclasses import dataclass
from typing import Any, Literal

from pydantic import BaseModel, Field


@dataclass
class EvaluationItem:
    """Single ground-truth example for agent answer evaluation.

    Attributes:
        question: User question to send to the agent.
        answer_orig: Expected answer used as the ground truth.
        document: Optional source identifier for the ground-truth answer.
    """

    question: str
    answer_orig: str
    document: str | None = None


@dataclass
class AgentAnswerRecord:
    """Generated agent answer with the trajectory used to produce it.

    Attributes:
        question: User question sent to the agent.
        answer_agent: Final answer generated by the agent.
        answer_orig: Expected ground-truth answer.
        tool_calls: Tool calls extracted from the agent event stream.
        document: Optional source identifier for the ground-truth answer.
    """

    question: str
    answer_agent: str
    answer_orig: str
    tool_calls: list[dict[str, Any]]
    document: str | None = None


class AgentEvaluation(BaseModel):
    """Structured LLM judge result for one agent answer.

    Attributes:
        answer_reasoning: Judge explanation for the answer-quality score.
        answer_score: `good` when the final answer matches the ground truth.
        trajectory_reasoning: Judge explanation for the tool-trajectory score.
        trajectory_score: `good` when tool calls were reasonable for the question.
    """

    answer_reasoning: str = Field(
        description="Reasoning about whether the final answer is correct."
    )
    answer_score: Literal["good", "bad"] = Field(
        description="'good' if the final answer matches the ground-truth answer."
    )
    trajectory_reasoning: str = Field(
        description="Reasoning about whether the tool calls were useful."
    )
    trajectory_score: Literal["good", "bad"] = Field(
        description="'good' if the tool calls were reasonable for the question."
    )


@dataclass
class EvaluationResult:
    """Final row stored for one judged agent answer.

    Attributes:
        question: User question sent to the agent.
        document: Optional source identifier for the ground-truth answer.
        answer_score: Judge score for final answer quality.
        trajectory_score: Judge score for agent tool usage.
        answer_reasoning: Judge explanation for final answer quality.
        trajectory_reasoning: Judge explanation for tool usage.
    """

    question: str
    document: str | None
    answer_score: Literal["good", "bad"]
    trajectory_score: Literal["good", "bad"]
    answer_reasoning: str
    trajectory_reasoning: str
